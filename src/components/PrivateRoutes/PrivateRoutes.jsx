import { Redirect, Route } from 'react-router-dom';
export default function PrivateRoutes({ children, ...rest }) {
  return (
    <Route
      {...rest}
      render={({ location }) =>
        auth.user ? (
          children
        ) : (
          <Redirect
            to={{
              pathname: '/login',
              state: { from: location },
            }}
          />
        )
      }
    />
  );
}
